        "    \"\"\"\n",
        "    results = []\n",
        "    for page in range(1, max_pages + 1):\n",
        "        data = {\n",
        "            \"pageNum\": page,\n",
        "            \"pageSize\": 30,\n",
        "            \"column\": \"szse\",\n",
        "            \"tabName\": \"fulltext\",\n",
        "            \"plate\": \"\",\n",
        "            \"stock\": \"\",\n",
        "            \"searchkey\": f\"{company} 招股说明书\",\n",
        "            \"secid\": \"\",\n",
        "            \"category\": \"\",\n",
        "            \"trade\": \"\",\n",
        "            \"seDate\": \"\",\n",
        "            \"sortName\": \"\",\n",
        "            \"sortType\": \"\",\n",
        "            \"isHLtitle\": \"true\",\n",
        "        }\n",
        "        try:\n",
        "            resp = requests.post(CNINFO_QUERY_URL, data=data, headers=HEADERS, timeout=20)\n",
        "            resp.raise_for_status()\n",
