        "# ========== 2. 工具函数 ==========\n",
        "\n",
        "def now_str():\n",
        "    return dt.datetime.now().strftime(\"%Y-%m-%d %H:%M:%S\")\n",
        "\n",
        "\n",
        "def safe_filename(name: str) -> str:\n",
        "    \"\"\"清理文件名中的非法字符。\"\"\"\n",
        "    return re.sub(r'[\\\\/:*?\"<>|]+', \"_\", name).strip()\n",
        "\n",
        "\n",
        "def title_is_target(title: str) -> bool:\n",
        "    \"\"\"判断公告标题是否符合正式招股说明书规则。\"\"\"\n",
        "    if not title:\n",
        "        return False\n",
        "    if not all(k in title for k in REQUIRED_KEYWORDS):\n",
        "        return False\n",
        "    if any(k in title for k in EXCLUDE_KEYWORDS):\n",
        "        return False\n",
        "    return True\n",
        "\n",
        "\n",
        "def get_pdf_page_count(pdf_path: Path):\n",
        "    \"\"\"读取 PDF 页数。读取失败返回 None。\"\"\"\n",
        "    try:\n",
        "        reader = PdfReader(str(pdf_path))\n",
        "        return len(reader.pages)\n",
        "    except Exception:\n",
        "        return None\n",
        "\n",
        "\n",
        "def file_size_mb(path: Path) -> float:\n",
        "    return path.stat().st_size / 1024 / 1024\n",
        "\n",
        "\n",
        "def guess_year_from_title(title: str) -> str:\n",
        "    \"\"\"从标题中提取年份，提取不到则用当前年份。\"\"\"\n",
        "    match = re.search(r\"(20\\\\d{2})\", title or \"\")\n",
