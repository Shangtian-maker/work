        "# ========== 5. 下载与校验 ==========\n",
        "\n",
        "def download_pdf(url: str, save_path: Path) -> tuple[bool, str]:\n",
        "    \"\"\"下载 PDF。返回 (是否成功, 错误信息)。\"\"\"\n",
        "    try:\n",
        "        with requests.get(url, headers=HEADERS, timeout=60, stream=True) as r:\n",
        "            r.raise_for_status()\n",
        "\n",
        "            with open(save_path, \"wb\") as f:\n",
        "                for chunk in r.iter_content(chunk_size=1024 * 128):\n",
        "                    if chunk:\n",
        "                        f.write(chunk)\n",
        "\n",
        "        if not save_path.exists() or save_path.stat().st_size == 0:\n",
        "            return False, \"下载后文件不存在或为空\"\n",
        "\n",
        "        with open(save_path, \"rb\") as f:\n",
        "            head = f.read(5)\n",
        "        if head != b\"%PDF-\":\n",
        "            return False, \"文件头不是 PDF，可能下载到了网页或错误页面\"\n",
        "\n",
        "        return True, \"\"\n",
        "    except Exception as e:\n",
        "        return False, str(e)\n",
        "\n",
        "\n",
